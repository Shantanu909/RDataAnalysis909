# Arithmetic Operators
x <- 10
y <- 5

# Addition
result <- x + y
print(result)  

# Subtraction
result <- x - y
print(result) 

# Multiplication
result <- x * y
print(result)  

# Division
result <- x / y
print(result)  

# Modulus (Remainder)
result <- x %% y
print(result)  # Expected output: 0

# Exponentiation
result <- x ^ y
print(result) 
# Relational Operators
a <- 5
b <- 10

# Equal to
result <- a == b
print(result)

# Not equal to
result <- a != b
print(result)

# Greater than
result <- a > b
print(result)

# Less than
result <- a < b
print(result)

# Greater than or equal to
result <- a >= b
print(result)

# Less than or equal to
result <- a <= b
print(result)

# Logical Operators
p <- TRUE
q <- FALSE

# Logical AND
result <- p && q
print(result)

# Logical OR
result <- p || q
print(result)  

# Logical NOT
result <- !p
print(result)  

# Assignment Operators
x <- 10
y <- 5

# Assignment
x <- y
print(x)  
# Addition assignment
x <- 10
x <- x + y
print(x)  

# Subtraction assignment
x <- 10
x <- x - y
print(x)  # Expected output: 5

# Multiplication assignment
x <- 10
x <- x * y
print(x)  # Expected output: 50

# Division assignment
x <- 10
x <- x / y
print(x)  

# Modulus (Remainder) assignment
x <- 10
x <- x %% y
print(x) 

# Miscellaneous Operators
x <- 10
y <- 5

# %in% operator (checks if value is present in a vector)
result <- x %in% c(5, 10)
print(result)  

# : operator (creates a sequence)
result <- 1:5
print(result)  

# $ operator (accesses elements of a list or data frame)
person <- list(name = "John", age = 25, city = "New York")
result <- person$name
print(result)  

